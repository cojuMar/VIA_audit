// HIGH: Token revocation
// ---------------------------------------------------------------------------
describe('[HIGH] Token Revocation', () => {
  it('Revoked access token is rejected on subsequent requests', async () => {
    // Issue a short-lived token for this test
    const shortLivedToken = await (async () => {
      const res = await post(`${AUTH_URL}/test/issue-token`, {
        tenantId: TENANT_A_ID,
        role: 'readonly',
      });
      const body = await res.json() as { accessToken: string };
      return body.accessToken;
    })();

    // Verify it works first
    const beforeRevoke = await get(`${AUTH_URL}/auth/me`, shortLivedToken);
    expect(beforeRevoke.status).toBe(200);

    // Revoke it
    const revokeRes = await post(`${AUTH_URL}/auth/token/revoke`, {}, shortLivedToken);
    expect(revokeRes.status).toBe(200);

    // Now it must be rejected
    const afterRevoke = await get(`${AUTH_URL}/auth/me`, shortLivedToken);
    expect(afterRevoke.status).toBe(401);
  });
});

