// HIGH: JWKS and token signing
// ---------------------------------------------------------------------------
describe('[HIGH] JWKS Endpoint', () => {
  it('JWKS endpoint returns valid JWK set without authentication', async () => {
    const res = await get(`${AUTH_URL}/.well-known/jwks.json`);
    expect(res.status).toBe(200);
    const body = await res.json() as { keys: Array<{ kty: string; alg: string; use: string }> };
    expect(body.keys).toBeDefined();
    expect(body.keys.length).toBeGreaterThan(0);
    const key = body.keys[0]!;
    expect(key.kty).toBe('RSA');
    expect(key.alg).toBe('RS256');
    expect(key.use).toBe('sig');
  });

  it('JWKS response includes appropriate cache headers', async () => {
    const res = await get(`${AUTH_URL}/.well-known/jwks.json`);
    const cacheControl = res.headers.get('cache-control');
    expect(cacheControl).toContain('max-age');
  });

  it('Private key material is NOT exposed in JWKS', async () => {
    const res = await get(`${AUTH_URL}/.well-known/jwks.json`);
    const body = await res.json() as { keys: Array<Record<string, unknown>> };
    for (const key of body.keys) {
      // RSA private key components must never appear
      expect(key['d']).toBeUndefined();
      expect(key['p']).toBeUndefined();
      expect(key['q']).toBeUndefined();
      expect(key['dp']).toBeUndefined();
      expect(key['dq']).toBeUndefined();
    }
  });
});

